SELECT DEPARTMENT_ID,concat(concat(DEPARTMENT_NAME,' '),LOCATION_ID) as "INFO"
FROM DEPARTMENTS
WHERE DEPARTMENT_ID >= '&dept'
ORDER BY &dept ;

