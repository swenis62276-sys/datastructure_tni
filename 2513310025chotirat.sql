--4
SELECT concat(concat (FIRST_NAME,' '),LAST_NAME) as EMP_NAME ,SALARY,COMMISSION_PCT
FROM EMPLOYEES
where FIRST_NAME LIKE'e'and SALARY in (2.0,0.4) 
ORDER BY COMMISSION_PCT;
